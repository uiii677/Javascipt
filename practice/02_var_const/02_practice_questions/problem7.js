
// Manually swap

let city1 = "Abbottabad"
let city2 = "Peshawar"

console.log("Before swapping:");
console.log("City1= " + city1 );
console.log("City2= " + city2 );

city1 = "Peshawar"
city2 = "Abbottabad"

console.log("After Swapping:");
console.log("City1= " + city1);
console.log("City2= " + city2);
